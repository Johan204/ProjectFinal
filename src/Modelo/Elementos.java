package Modelo;

/**
 *
 * @author USUARIO
 */
public abstract class Elementos implements ObjectsMind{
    protected int posX;
    protected int posY;

    public Elementos(int posX, int posY) {
        this.posX = posX;
        this.posY = posY;
    }

    public int getPosX() {
        return posX;
    }

    public int getPosY() {
        return posY;
    }
    
    
    
    
    
}
